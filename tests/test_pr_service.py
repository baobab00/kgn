class TestPRList:
    def test_list_prs(self) -> None:
        """list_prs returns PR list from API."""

        def handler(request: httpx.Request) -> httpx.Response:
            if "/pulls" in str(request.url):
                return httpx.Response(
                    200,
                    json=[
                        {"number": 1, "title": "PR 1", "state": "open"},
                        {"number": 2, "title": "PR 2", "state": "open"},
                    ],
                )
            return httpx.Response(404)

        client = _mock_client(handler)
        pr_svc = PullRequestService(client)
        prs = pr_svc.list_prs(state="open")

        assert len(prs) == 2
        assert prs[0]["number"] == 1


# ══════════════════════════════════════════════════════════════════════
#  PRResult dataclass
# ══════════════════════════════════════════════════════════════════════


class TestPRResult:
    def test_defaults(self) -> None:
        result = PRResult(success=False)
        assert result.pr_number is None
        assert result.html_url is None
        assert result.message == ""

    def test_with_values(self) -> None:
        result = PRResult(
            success=True,
            pr_number=42,
            html_url="https://example.com/pr/42",
            message="Created",
        )
        assert result.pr_number == 42
